        public override void OnDoubleClick( Mobile from )
        {
            if ( !IsChildOf( from.Backpack ) )
            {
                from.SendLocalizedMessage( 1042001 ); // that must be in your pack
                return;
            }

            // Pick a random location
            var dest = m_Locations[Utility.Random( m_Locations.Length )];

            // Teleport the player
            from.SendMessage( "The map pulses with dark energy..." );
            from.MoveToWorld( new Point3D( dest.X, dest.Y, dest.Z ), dest.Map );
            from.PlaySound( 0x1FE ); // optional teleport sound

            // Consume the map
            this.Delete();
        }
