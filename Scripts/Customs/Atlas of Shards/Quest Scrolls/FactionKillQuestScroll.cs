        ) : base(0x14EF)  // parchment graphic
        {
            Weight   = 1.0;
            LootType = LootType.Blessed;
            Hue      = 0x4B5;

            // Pick monster
            var pick = (customMonster != null
                        ? DefaultMonsterPool.FirstOrDefault(m => m.type == customMonster)
                        : DefaultMonsterPool[Utility.Random(DefaultMonsterPool.Length)]);
            _monsterType  = pick.type ?? DefaultMonsterPool[0].type;
            _monsterName  = pick.name ?? DefaultMonsterPool[0].name;

            // Counts & rewards
            _amountToKill = (customAmount > 0) ? customAmount : Utility.RandomMinMax(10, 20);
            _faction      = faction;
            _repReward    = (customRep > 0) ? customRep : _amountToKill * 50;  // scale as desired

            _rewardTypes  = (customRewards != null && customRewards.Length > 0)
                            ? customRewards.ToList()
                            : PickRandomRewards();

            Name = $"Kill Quest: slay {_amountToKill} {_monsterName}";
        }

        private static List<Type> PickRandomRewards()
        {
            int n = Utility.RandomMinMax(1, 4);
            return DefaultRewardPool
                   .OrderBy(_ => Utility.Random(0x10000))
                   .Take(n)
                   .ToList();
        }

        // --------------------------------------------------------------------
        // ⓹  HELPERS
        // --------------------------------------------------------------------
        private static void GiveFactionRep(Mobile player, XmlMobFactions.GroupTypes faction, int amount)
        {
            if (amount <= 0) return;

            var x = XmlAttach.FindAttachment(player, typeof(XmlMobFactions), "Standard") as XmlMobFactions;
            if (x == null)
            {
                x = new XmlMobFactions();
                XmlAttach.AttachTo(player, player, x);
            }

            int current = x.GetFactionLevel(faction);
            x.SetFactionLevel(faction, current + amount);
            player.SendMessage($"You gain {amount} reputation with {faction}!");
        }

        // --------------------------------------------------------------------
        // ⓺  DOUBLE-CLICK → SHOW GUMP
        // --------------------------------------------------------------------
        public override void OnDoubleClick(Mobile from)
        {
            if (!IsChildOf(from.Backpack))
            {
                from.SendLocalizedMessage(1047012); // must be in backpack
                return;
            }

            from.SendGump(new KillQuestGump(from, this));
        }

        private class KillQuestGump : Gump
        {
            private readonly FactionKillQuestScroll _scroll;

            public KillQuestGump(Mobile from, FactionKillQuestScroll scroll)
                : base(75, 50)
            {
                _scroll = scroll;

                int rewardLines = _scroll._rewardTypes.Count;
                int height      = 160 + rewardLines * 25;

                AddPage(0);
                AddBackground(0, 0, 300, height, 9270);

                AddLabel(40, 20, 54, "Kill Quest");

                int y = 50;
                AddLabel(40, y, 54, $"Need:   {_scroll._amountToKill} {_scroll._monsterName}"); y += 20;
                AddLabel(40, y, 54, $"Killed: {_scroll._amountKilled}");                        y += 20;
                AddLabel(40, y, 54, $"Faction Rep: {_scroll._repReward} ({_scroll._faction})"); y += 20;

                AddLabel(40, y, 54, "Items:"); y += 20;
                foreach (var t in _scroll._rewardTypes)
                {
                    AddLabel(60, y, 54, t.Name);
                    y += 20;
                }

                if (_scroll._amountKilled < _scroll._amountToKill)
                {
                    AddButton(40, y, 4005, 4007, 1, GumpButtonType.Reply, 0);
                    AddLabel(75, y, 54, "Add Kill");
                }
                else
                {
                    AddButton(40, y, 4005, 4007, 2, GumpButtonType.Reply, 0);
                    AddLabel(75, y, 54, "Claim Reward");
                }
            }

            public override void OnResponse(NetState state, RelayInfo info)
            {
                if (info.ButtonID == 1)
                    state.Mobile.Target = new KillQuestCorpseTarget(_scroll);
                else if (info.ButtonID == 2)
                    _scroll.TryClaim(state.Mobile);
            }
        }

        private class KillQuestCorpseTarget : Target
        {
            private readonly FactionKillQuestScroll _scroll;

            public KillQuestCorpseTarget(FactionKillQuestScroll scroll)
                : base(1, false, TargetFlags.None)
            {
                _scroll = scroll;
            }

            protected override void OnTarget(Mobile from, object targeted)
            {
                if (targeted is Corpse corpse
                    && corpse.Owner is BaseCreature bc
                    && bc.GetType() == _scroll._monsterType)
                {
                    _scroll._amountKilled++;
                    from.SendMessage("Kill recorded.");

                    corpse.Delete();

                    if (_scroll._amountKilled < _scroll._amountToKill)
                        from.Target = new KillQuestCorpseTarget(_scroll);
                    else
                        from.SendGump(new KillQuestGump(from, _scroll));
                }
                else
                {
                    from.SendMessage("That corpse does not match your quest.");
                }
            }
        }

        // --------------------------------------------------------------------
        // ❽  CLAIM REWARD
        // --------------------------------------------------------------------
        private void TryClaim(Mobile from)
        {
            if (_amountKilled < _amountToKill)
            {
                from.SendMessage("You have not completed the slaying yet.");
                return;
            }

            // Award faction reputation instead of gold
            GiveFactionRep(from, _faction, _repReward);

            // Grant item rewards
            foreach (var t in _rewardTypes)
                if (Activator.CreateInstance(t) is Item item)
                    from.AddToBackpack(item);

            from.SendMessage("Your rewards have been placed in your backpack!");
            Delete();
        }

        // --------------------------------------------------------------------
        // ❾  PERSISTENCE
        // --------------------------------------------------------------------
        public FactionKillQuestScroll(Serial serial) : base(serial) { }

        public override void Serialize(GenericWriter writer)
        {
            base.Serialize(writer);
            writer.Write(1); // version

            writer.Write(_monsterType?.AssemblyQualifiedName ?? "");
            writer.Write(_monsterName);
            writer.Write(_amountToKill);
            writer.Write(_amountKilled);
            writer.Write((int)_faction);
            writer.Write(_repReward);

            writer.Write(_rewardTypes.Count);
            foreach (var t in _rewardTypes)
                writer.Write(t.AssemblyQualifiedName);
        }

        public override void Deserialize(GenericReader reader)
        {
            base.Deserialize(reader);
            _ = reader.ReadInt(); // version

            _monsterType  = Type.GetType(reader.ReadString());
            _monsterName  = reader.ReadString();
            _amountToKill = reader.ReadInt();
            _amountKilled = reader.ReadInt();
            _faction      = (XmlMobFactions.GroupTypes)reader.ReadInt();
            _repReward    = reader.ReadInt();

            int count = reader.ReadInt();
            _rewardTypes = new List<Type>();
            for (int i = 0; i < count; i++)
            {
                var t = Type.GetType(reader.ReadString());
                if (t != null)
                    _rewardTypes.Add(t);
            }
        }
