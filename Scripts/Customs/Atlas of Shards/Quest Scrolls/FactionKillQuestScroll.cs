        [Constructable]  // Enables: [add FactionKillQuestScroll]
        public FactionKillQuestScroll()
            : this(null, 0, XmlMobFactions.GroupTypes.Player, 0, new Type[0])
        {
        }

		// --------------------------------------------------------------------
		// ❹c  XML-SPAWNER STRING-BASED CONSTRUCTOR
		//    Matches ctorArgs="Dragon,5,Humanoid,500" coming in as strings.
		// --------------------------------------------------------------------
		[Constructable]
		public FactionKillQuestScroll(
			string monsterNameStr,   // e.g. "Dragon"
			string amountStr,        // e.g. "5"
			string factionStr,       // e.g. "Humanoid"
			string repStr            // e.g. "500"
		) : base(0x14EF)             // parchment graphic (we won't use fields here)
		{
			// Parse count
			int customAmount = 1;
			if (!Int32.TryParse(amountStr, out customAmount) || customAmount < 1)
				customAmount = 1;

			// Parse faction enum
			XmlMobFactions.GroupTypes faction = XmlMobFactions.GroupTypes.Player;
			try
			{
				faction = (XmlMobFactions.GroupTypes) Enum.Parse(
					typeof(XmlMobFactions.GroupTypes),
					factionStr,
					ignoreCase: true
				);
			}
			catch
			{
				faction = XmlMobFactions.GroupTypes.Player;
			}

			// Parse reputation
			int customRep = 0;
			if (!Int32.TryParse(repStr, out customRep) || customRep < 0)
				customRep = 0;

			// Lookup monster Type by name
			Type monsterType = DefaultMonsterPool
				.FirstOrDefault(m => 
					m.name.Equals(monsterNameStr, 
								  StringComparison.InvariantCultureIgnoreCase))
				.type
				?? DefaultMonsterPool[0].type;

			// Now delegate into the fully-typed ctor
			// Use Utility.RandomRewards for rewards, passing empty explicit rewards
			Weight   = 1.0;
			LootType = LootType.Blessed;
			Hue      = 0x4B5;

			_monsterType  = monsterType;
			_monsterName  = monsterNameStr;
			_amountToKill = customAmount;
			_faction      = faction;
			_repReward    = customRep;
			_rewardTypes  = new List<Type>(); // no extra rewards

			Name = $"Kill Quest: slay {_amountToKill} {_monsterName}";
		}

		// --------------------------------------------------------------------
		// ❹d  XML-SPAWNER  (string, int, string, int)
		//      Matches ctorArgs like "Dragon,5,Humanoid,500"
		// --------------------------------------------------------------------
		[Constructable]
		public FactionKillQuestScroll(
			string monsterName,   // "Dragon"
			int    amount,        // 5
			string factionStr,    // "Humanoid"
			int    rep            // 500
		) : this(
			// resolve monster type from the name
			DefaultMonsterPool
				.FirstOrDefault(m => m.name.Equals(monsterName,
												   StringComparison.InvariantCultureIgnoreCase))
				.type,
			amount,
			// parse faction enum (defaults to Player if bad string)
			Enum.TryParse(factionStr, true, out XmlMobFactions.GroupTypes fac)
				? fac
				: XmlMobFactions.GroupTypes.Player,
			rep,
			// no extra item rewards
			Array.Empty<Type>()
		)
		{
		}

		// --------------------------------------------------------------------
		// ❹e  XML-SPAWNER  (string, int, XmlMobFactions.GroupTypes, int)
		//      Covers the case where XmlSpawner has already parsed the faction
		//      string into the enum value before reflection.
		// --------------------------------------------------------------------
		[Constructable]
		public FactionKillQuestScroll(
			string                       monsterName, // "Dragon"
			int                          amount,      // 5
			XmlMobFactions.GroupTypes    faction,     // Humanoid
			int                          rep          // 500
		) : this(
			DefaultMonsterPool
				.FirstOrDefault(m => m.name.Equals(monsterName,
												   StringComparison.InvariantCultureIgnoreCase))
				.type,
			amount,
			faction,
			rep,
			Array.Empty<Type>()
		)
		{
		}


