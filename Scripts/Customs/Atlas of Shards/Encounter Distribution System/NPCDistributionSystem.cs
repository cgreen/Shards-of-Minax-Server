    // --- Helper classes, exactly as in EncounterDistributionSystem ---
    internal sealed class NPCSpawnEntry
    {
        public string Text { get; }
        public int Count { get; }
        public NPCSpawnEntry(string text, int count)
        {
            Text = text;
            Count = count;
        }
        public XmlSpawner.SpawnObject ToSpawnObject()
            => new XmlSpawner.SpawnObject(Text, Count);
    }

    internal sealed class NPCDistributionDefinition
    {
        public string Name { get; }
        public int Weight { get; }
        public XmlSpawner.SpawnObject[] SpawnObjects { get; }
        public NPCDistributionDefinition(string name, int weight, IEnumerable<NPCSpawnEntry> entries)
        {
            Name = name;
            Weight = Math.Max(1, weight);
            SpawnObjects = entries.Select(e => e.ToSpawnObject()).ToArray();
        }
    }

    internal readonly struct NPCSpawnPoint
    {
        public Map Facet { get; }
        public Point3D Loc { get; }
        public NPCSpawnPoint(Map facet, Point3D loc)
        {
            Facet = facet;
            Loc = loc;
        }
    }

