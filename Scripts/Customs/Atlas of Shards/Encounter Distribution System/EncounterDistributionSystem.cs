    internal sealed class SpawnEntry
    {
        public string Text  { get; }
        public int    Count { get; }

        public SpawnEntry(string text, int count)
        {
            Text  = text;
            Count = count;
        }

        public XmlSpawner.SpawnObject ToSpawnObject()
            => new XmlSpawner.SpawnObject(Text, Count);
    }

    internal sealed class EncounterDefinition
    {
        public string Name { get; }
        public int Weight { get; }
        public XmlSpawner.SpawnObject[] SpawnObjects { get; }

        public EncounterDefinition(string name, int weight, IEnumerable<SpawnEntry> entries)
        {
            Name         = name;
            Weight       = Math.Max(1, weight);
            SpawnObjects = entries.Select(e => e.ToSpawnObject()).ToArray();
        }
    }

    internal readonly struct SpawnPoint
    {
        public Map     Facet { get; }
        public Point3D Loc   { get; }

        public SpawnPoint(Map facet, Point3D loc)
        {
            Facet = facet;
            Loc   = loc;
        }
    }

